    HABITACION=='doble'->(
          hotel(_,Nombre,Direccion,ESTR,_,PRES,_,IdDepartamento),
          departamento(IdDepartamento,_,_,_,CLI),
          ESTR==ESTRELLAS,
          CLI==CLIMA,
          /*PRES =< PRESUPUESTO,*/

          split_string(Direccion, " ", "", L),
          get_first(L,C),get_last(L,D),get_first(D,E),(
          C == "Km"->(
                   atom_number(E,DIST)
               );
          C == "Zona"->(
                   DIST=0
               )
          ),
          DIST =< DISTANCIA,(
          VEHICULO=='si'->(
                   COSTOVEHICULO is(2.5*DIST));
          VEHICULO=='no'->(
                   COSTOVEHICULO=0)),
          COSTODIAS is(DIAS*PRES),
          COSTOTOTAL is(COSTODIAS+COSTOVEHICULO),
          PRESUPUESTO >= COSTOTOTAL,

           write('-------------------------'),nl,
           write('Hotel: '),write(Nombre),nl,
           write('Direccion: '),write(Direccion),nl,
           write('Costo Hotel: '),write(COSTODIAS),nl,
